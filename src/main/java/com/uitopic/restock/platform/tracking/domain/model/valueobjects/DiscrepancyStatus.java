package com.uitopic.restock.platform.tracking.domain.model.valueobjects;

/**
 * Enumeration representing the status of a discrepancy in inventory tracking. It indicates whether a discrepancy has been resolved or remains unresolved. This value object is used to track the resolution status of inventory discrepancies within the system.
 */
public enum DiscrepancyStatus {
    RESOLVED,
    UNRESOLVED
}
